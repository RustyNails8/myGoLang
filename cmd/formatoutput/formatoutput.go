package main

import (
	"fmt"
)

func main() {
	fmt.Printf("Float ....   %f\n", 3.1445656)
	fmt.Printf("Integer ..   %d\n", 3)
	fmt.Printf("String ...   %s\n", "Sumit")
	fmt.Printf("Boolean ..   %t\n", true)
	fmt.Printf("Values ...   %v %v %v\n", 3.14, "\t", "Sumit")
	fmt.Printf("Golang ...   %#v\n", "\t")
	fmt.Printf("Type .....   %T %T %T\n", "\t", "Sumit", true)

}
